//program for unary operator
#include <stdio.h>

int main()
{
    int a = 54, b = 65;  
    //Declare the values a&b

    // Unary Operator
    printf("Unary Operators:\n");

    printf("Increment a: %d\n", ++a);   
    // This will give the increment of the give value in A

    printf("Decrement b: %d\n", --b);   
    // This will give the decrement of the give value in B

    return 0;
}